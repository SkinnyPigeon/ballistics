# t = time
# y = height
# x = distance
# V = vertical velocity
# V0 = initial vertical velocity

# U = horizontal velocity (is a constant unless acted on by an external force such a wind)
# g = gravitational acceleration

# horizontal component
# U = U0
# x = U0t

x = U * t

# vertical component
# V = V0 - g t
# y = V0t - .5 g t**2

# highest point
# V = 0
# t = V0 / g
# h = .5 V0 ** 2 / g

# at ground impact
# y = 0
# t = 2 V0 / g
# V = -V0

